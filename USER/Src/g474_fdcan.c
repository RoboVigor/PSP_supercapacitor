uint8_t Get_CRC8_Check_Sum(unsigned char *pchMessage, unsigned int dwLength) {
    unsigned char ucCRC8 = CRC8_INIT;
    unsigned char ucIndex;
    while (dwLength--) {
        ucIndex = ucCRC8 ^ (*pchMessage++);
        ucCRC8  = CRC8_TAB[ucIndex];
    }
    return (ucCRC8);
}

