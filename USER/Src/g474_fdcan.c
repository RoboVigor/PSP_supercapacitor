/*
** Descriptions: CRC16 checksum function
** Input: Data to check,channel length, initialized checksum
** Output: CRC checksum
*/
uint16_t Get_CRC16_Check_Sum(unsigned char *pchMessage, unsigned int dwLength) {
    unsigned short wCRC = CRC_INIT;
    unsigned char  chData;
    if (pchMessage == 0) {
        return 0xFFFF;
    }
    while (dwLength--) {
        chData = *pchMessage++;
        (wCRC) = ((unsigned short) (wCRC) >> 8) ^ wCRC_Table[((unsigned short) (wCRC) ^ (unsigned short) (chData)) & 0x00ff];
    }
    return wCRC;
}